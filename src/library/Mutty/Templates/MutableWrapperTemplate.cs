using System;
using System.Collections.Generic;
using System.Collections.Immutable;
using System.Linq;
using Mutty.CodeHelpers;
using Mutty.Models;

namespace Mutty.Templates;

/// <summary>
/// A template that generates the mutable wrapper for a record.
/// </summary>
/// <param name="tokens">The model for the record.</param>
public class MutableWrapperTemplate(RecordModel tokens) : IndentedCodeBuilder
{
    private readonly string? _namespaceName = tokens.NamespaceName;
    private readonly string _recordName = tokens.RecordName;
    private readonly string _mutableRecordName = tokens.MutableRecordName;
    private readonly ImmutableArray<PropertyModel> _properties = tokens.Properties.AsImmutableArray();

    /// <summary>
    /// Generates the code for the mutable wrapper.
    /// </summary>
    /// <returns>The generated code.</returns>
    public string GenerateCode()
    {
        CommentAutoGenerated();
        Line("#nullable enable");
        EmptyLine();
        Line("using System.Collections.Generic;");
        Line("using System.Collections.Immutable;");
        Line("using System.Linq;");
        EmptyLine();
        if (_namespaceName is not null)
        {
            Line($"namespace {_namespaceName}");
            Braces(GenerateClass);
        }
        else
        {
            GenerateClass();
        }

        return ToString();
    }

    private void GenerateClass()
    {
        Summary($"The mutable wrapper for the <see cref=\"{_recordName}\"/> record.");
        Line($"public partial class {_mutableRecordName}");
        Braces(() =>
        {
            Line($"private {_recordName} _record;");
            GenerateConstructor();
            GenerateBuilderMethod();
            GenerateToImmutableAlias();
            GenerateOnBeforeBuildHook();
            GenerateImplicitOperatorToMutable();
            GenerateImplicitOperatorToRecord();
            GenerateProperties();
        });
    }

    private void GenerateConstructor()
    {
        EmptyLine();
        Summary($"Initializes a new instance of the <see cref=\"{_mutableRecordName}\"/> class.");
        Line($"/// <param name=\"record\">The record to wrap.</param>");
        Line($"public {_mutableRecordName}({_recordName} record)");
        Braces(() =>
        {
            Line("_record = record;");
            EmptyLine();
            foreach (PropertyModel property in _properties)
            {
                switch (property.PropertyType)
                {
                    case PropertyType.ImmutableCollection:
                        {
                            Line($"{property.Name} = {GenerateCollectionIngestExpression(property)};");
                            break;
                        }
                    case PropertyType.Record:
                        {
                            // Handle nullable record types to avoid NullReferenceException
                            // when implicit conversion is applied to null values
                            bool isNullable = property.Type.EndsWith("?", StringComparison.Ordinal);
                            if (isNullable)
                            {
                                string mutableTypeName = $"Mutable{property.Type.TrimEnd('?').Split('.').Last()}";
                                Line($"{property.Name} = _record.{property.Name} != null");
                                Indent(() =>
                                {
                                    Line($"? new {mutableTypeName}(_record.{property.Name})");
                                    Line(": null;");
                                });
                            }
                            else
                            {
                                Line($"{property.Name} = _record.{property.Name};");
                            }

                            break;
                        }
                    case PropertyType.Other:
                        {
                            Line($"{property.Name} = _record.{property.Name};");
                            break;
                        }
                }
            }
        });
    }

    private void GenerateBuilderMethod()
    {
        EmptyLine();
        Summary($"Builds a new instance of the <see cref=\"{_recordName}\"/> class.");
        Line($"public {_recordName} Build()");
        Braces(() =>
        {
            Line("OnBeforeBuild();");
            Line("return _record with");
            Braces(
                () =>
                {
                    foreach (PropertyModel property in _properties)
                    {
                        switch (property.PropertyType)
                        {
                            case PropertyType.Record:
                                {
                                    Line($"{property.Name} = this.{property.Name},");
                                    break;
                                }
                            case PropertyType.ImmutableCollection:
                                {
                                    Line($"{property.Name} = {GenerateCollectionBuildExpression(property)},");
                                    break;
                                }
                            case PropertyType.Other:
                                {
                                    Line($"{property.Name} = this.{property.Name},");
                                    break;
                                }
                            default:
                                throw new ArgumentOutOfRangeException();
                        }
                    }
                },
                ";");
        });
    }

    private void GenerateToImmutableAlias()
    {
        EmptyLine();
        Summary($"Builds a new instance of the <see cref=\"{_recordName}\"/> record. Alias for <see cref=\"Build\"/>.");
        Line($"public {_recordName} ToImmutable()");
        Braces(() => Line("return Build();"));
    }

    private void GenerateOnBeforeBuildHook()
    {
        EmptyLine();
        Summary("Called at the start of <see cref=\"Build\"/>, before the immutable record is produced.");
        Line("/// Implement this method in a user-defined partial class to validate or normalise state.");
        Line("partial void OnBeforeBuild();");
    }

    private void GenerateProperties()
    {
        foreach (PropertyModel property in _properties)
        {
            EmptyLine();

            switch (property.PropertyType)
            {
                case PropertyType.Record:
                    {
                        GenerateNestedMutableProperty(property);
                        break;
                    }
                case PropertyType.ImmutableCollection:
                    {
                        GenerateCollectionProperty(property);
                        break;
                    }
                case PropertyType.Other:
                    {
                        GenerateSimpleProperty(property);
                        break;
                    }
                default:
                    throw new ArgumentOutOfRangeException();
            }
        }
    }

    private void GenerateSimpleProperty(PropertyModel property)
    {
        Summary($"Gets or sets the {property.Name}.");
        Line($"public {property.Type} {property.Name} {{ get; set; }}");
    }

    private void GenerateNestedMutableProperty(PropertyModel property)
    {
        bool isNullable = property.Type.EndsWith("?", StringComparison.Ordinal);
        string baseType = (isNullable) ? property.Type.TrimEnd('?') : property.Type;
        string mutableTypeName = $"Mutable{baseType.Split('.').Last()}";
        string finalType = (isNullable) ? $"{mutableTypeName}?" : mutableTypeName;
        string propertyType = property.PropertyType.ToString();

        Summary($"Gets or sets the {propertyType} {property.Name}.");
        Line($"public {finalType} {property.Name} {{ get; set; }}");
    }

    private void GenerateCollectionProperty(PropertyModel property)
    {
        string mutableType = ConvertImmutableToMutable(property.Type);
        string typeArgs = GetMutableItemType(property.Type);
        string propertyType = property.PropertyType.ToString();

        // Split all type arguments, convert each individually (handles multi-arg generics like Dictionary<K,V>)
        string convertedTypeArgs = ConvertGenericTypeArguments(typeArgs);

        Summary($"Gets or sets the {propertyType} {property.Name}.");
        Line($"public {mutableType}<{convertedTypeArgs}> {property.Name} {{ get; set; }}");
    }

    private string GenerateCollectionIngestExpression(PropertyModel property)
    {
        string source = $"_record.{property.Name}";

        // List-like collections preserve the existing behaviour, including the AsMutable() path that
        // wraps elements when they are themselves [MutableGeneration] records.
        if (IsListLikeImmutable(property.Type))
        {
            string itemType = GetMutableItemType(property.Type);
            return (IsBuiltInType(itemType))
                ? $"{source}.ToList()"
                : $"{source}.AsMutable()";
        }

        // Dictionary / set / queue / stack: construct the concrete mutable collection from the source.
        string mutableType = ConvertImmutableToMutable(property.Type);
        string typeArgs = ConvertGenericTypeArguments(GetMutableItemType(property.Type));
        return $"new {mutableType}<{typeArgs}>({source})";
    }

    private string GenerateCollectionBuildExpression(PropertyModel property)
    {
        string source = $"this.{property.Name}";

        if (IsListLikeImmutable(property.Type))
        {
            string itemType = GetMutableItemType(property.Type);
            return (IsBuiltInType(itemType))
                ? $"{source}.{GetImmutableConversionMethod(property.Type)}"
                : $"{source}.ToImmutable()";
        }

        string shortName = GetImmutableShortName(property.Type);
        return shortName switch
        {
            "ImmutableDictionary" => $"{source}.ToImmutableDictionary()",
            "ImmutableSortedDictionary" => $"{source}.ToImmutableSortedDictionary()",
            "ImmutableHashSet" => $"{source}.ToImmutableHashSet()",
            "ImmutableSortedSet" => $"{source}.ToImmutableSortedSet()",
            "ImmutableQueue" => $"ImmutableQueue.CreateRange({source})",
            "ImmutableStack" => $"ImmutableStack.CreateRange({source})",
            _ => $"{source}.ToImmutableList()"
        };
    }

    private bool IsListLikeImmutable(string immutableType)
    {
        string shortName = GetImmutableShortName(immutableType);
        return shortName is "ImmutableArray" or "ImmutableList";
    }

    private string GetImmutableShortName(string immutableType)
    {
        int genericIndex = immutableType.IndexOf('<');
        string withoutArgs = (genericIndex > 0) ? immutableType.Substring(0, genericIndex) : immutableType;
        return withoutArgs.Split('.').Last();
    }

    private string ConvertImmutableToMutable(string immutableType)
    {
        Dictionary<string, string> immutableMap = [];
        immutableMap.Add("System.Collections.Immutable.ImmutableArray", "List");
        immutableMap.Add("System.Collections.Immutable.ImmutableDictionary", "Dictionary");
        immutableMap.Add("System.Collections.Immutable.ImmutableHashSet", "HashSet");
        immutableMap.Add("System.Collections.Immutable.ImmutableList", "List");
        immutableMap.Add("System.Collections.Immutable.ImmutableQueue", "Queue");
        immutableMap.Add("System.Collections.Immutable.ImmutableSortedDictionary", "SortedDictionary");
        immutableMap.Add("System.Collections.Immutable.ImmutableSortedSet", "SortedSet");
        immutableMap.Add("System.Collections.Immutable.ImmutableStack", "Stack");

        // use immutableMap and StartsWith
        foreach (KeyValuePair<string, string> pair in immutableMap)
        {
            string key = pair.Key;
            string value = pair.Value;

            if (immutableType.StartsWith(key, StringComparison.Ordinal))
            {
                return value;
            }
        }

        // Handle other types as needed
        return immutableType;
    }

    private string GetMutableItemType(string immutableType)
    {
        int genericTypeIndex = immutableType.IndexOf('<');
        if (genericTypeIndex > 0)
        {
            // Extract everything between the outermost < and >
            // Don't split by '.' here as it breaks nested generics like ImmutableDictionary<string, int>
            string itemType = immutableType.Substring(genericTypeIndex + 1, immutableType.Length - genericTypeIndex - 2);
            return itemType;
        }

        return immutableType;
    }

    private string GetImmutableConversionMethod(string immutableType)
    {
        if (immutableType.StartsWith("System.Collections.Immutable.ImmutableArray", StringComparison.Ordinal))
        {
            return "ToImmutableArray()";
        }

        if (immutableType.StartsWith("System.Collections.Immutable.ImmutableHashSet", StringComparison.Ordinal))
        {
            return "ToImmutableHashSet()";
        }

        if (immutableType.StartsWith("System.Collections.Immutable.ImmutableSortedSet", StringComparison.Ordinal))
        {
            return "ToImmutableSortedSet()";
        }

        return "ToImmutableList()"; // default for ImmutableList + others
    }

    private bool IsBuiltInType(string typeName)
    {
        // Strip nullable suffix before checking — nullable primitives (e.g. string?, int?)
        // are still value/primitive types and should not get the "Mutable" prefix.
        string baseTypeName = (typeName.EndsWith("?", StringComparison.Ordinal))
            ? typeName.TrimEnd('?')
            : typeName;

        // C# built-in types and common .NET types that should not get "Mutable" prefix
        string[] builtInTypes =
        [
            "string", "int", "long", "short", "byte", "sbyte",
            "uint", "ulong", "ushort", "decimal", "double", "float",
            "bool", "char", "object",
            "DateTime", "DateTimeOffset", "TimeSpan", "Guid",
            "Int16", "Int32", "Int64", "UInt16", "UInt32", "UInt64",
            "Single", "Double", "Decimal", "Boolean", "String", "Char", "Object",
            "Byte", "SByte"
        ];

        return builtInTypes.Contains(baseTypeName);
    }

    private string ConvertTypeToMutable(string typeName)
    {
        // Handle nullable types (e.g., "string?" → "string")
        bool isNullable = typeName.EndsWith("?", StringComparison.Ordinal);
        string baseType = (isNullable) ? typeName.TrimEnd('?') : typeName;

        // Check if it's a built-in type
        if (IsBuiltInType(baseType))
        {
            return typeName; // Return as-is (with nullable marker if present)
        }

        // Check if it's a generic type (contains '<')
        int genericIndex = baseType.IndexOf('<');
        if (genericIndex > 0)
        {
            // Extract the base generic type and type arguments
            string genericBaseName = baseType.Substring(0, genericIndex);
            string typeArgs = baseType.Substring(genericIndex + 1, baseType.Length - genericIndex - 2);

            // Convert immutable collection to mutable collection
            string mutableBaseName = ConvertImmutableToMutableSimple(genericBaseName);

            // Recursively convert type arguments
            string convertedTypeArgs = ConvertGenericTypeArguments(typeArgs);

            return $"{mutableBaseName}<{convertedTypeArgs}>{((isNullable) ? "?" : string.Empty)}";
        }

        // It's a custom record type - add "Mutable" prefix
        string mutableType = $"Mutable{baseType.Split('.').Last()}";
        return $"{mutableType}{((isNullable) ? "?" : string.Empty)}";
    }

    private string ConvertGenericTypeArguments(string typeArgs)
    {
        // Split type arguments by comma, handling nested generics properly
        List<string> args = SplitGenericArguments(typeArgs);
        List<string> convertedArgs = args.Select(arg => ConvertTypeToMutable(arg.Trim())).ToList();
        return string.Join(", ", convertedArgs);
    }

    private List<string> SplitGenericArguments(string typeArgs)
    {
        List<string> result = [];
        int depth = 0;
        int startIndex = 0;

        for (int i = 0; i < typeArgs.Length; i++)
        {
            char c = typeArgs[i];
            
            if (c == '<')
            {
                depth++;
            }
            else if (c == '>')
            {
                depth--;
            }
            else if (c == ',' && depth == 0)
            {
                result.Add(typeArgs.Substring(startIndex, i - startIndex));
                startIndex = i + 1;
            }
        }

        // Add the last argument
        if (startIndex < typeArgs.Length)
        {
            result.Add(typeArgs.Substring(startIndex));
        }

        return result;
    }

    private string ConvertImmutableToMutableSimple(string typeName)
    {
        // Simple mapping for immutable collection short names
        Dictionary<string, string> immutableMap = new()
        {
            { "ImmutableArray", "List" },
            { "ImmutableDictionary", "Dictionary" },
            { "ImmutableHashSet", "HashSet" },
            { "ImmutableList", "List" },
            { "ImmutableQueue", "Queue" },
            { "ImmutableSortedDictionary", "SortedDictionary" },
            { "ImmutableSortedSet", "SortedSet" },
            { "ImmutableStack", "Stack" }
        };

        // Extract the short type name (e.g., "ImmutableList" from "System.Collections.Immutable.ImmutableList")
        string shortName = typeName.Split('.').Last();

        // Check if it matches any immutable collection type
        if (immutableMap.TryGetValue(shortName, out string? mutableName))
        {
            return mutableName;
        }

        // Also check the full name for exact match
        const string fullPrefix = "System.Collections.Immutable.";
        if (typeName.StartsWith(fullPrefix, StringComparison.Ordinal))
        {
            string typeWithoutPrefix = typeName.Substring(fullPrefix.Length);
            if (immutableMap.TryGetValue(typeWithoutPrefix, out string? mutableNameFromFull))
            {
                return mutableNameFromFull;
            }
        }

        return typeName;
    }

    private void GenerateImplicitOperatorToMutable()
    {
        EmptyLine();
        Summary($"Performs an implicit conversion from <see cref=\"{_recordName}\"/> to <see cref=\"{_mutableRecordName}\"/>.");
        Line($"public static implicit operator {_mutableRecordName}({_recordName} record)");
        Braces(() => Line($"return new {_mutableRecordName}(record);"));
    }

    private void GenerateImplicitOperatorToRecord()
    {
        EmptyLine();
        Summary($"Performs an implicit conversion from <see cref=\"{_mutableRecordName}\"/> to <see cref=\"{_recordName}\"/>.");
        Line($"public static implicit operator {_recordName}({_mutableRecordName} mutable)");
        Braces(() => Line("return mutable.Build();"));
    }
}
